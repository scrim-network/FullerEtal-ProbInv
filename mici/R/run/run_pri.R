cmd    <- paste(commandArgs(), collapse=" ")
prior  <-            sub(".*pri_(.*)_.*",     "\\1", cmd)
nbatch <- as.numeric(sub(".*pri_.*_(.*)\\.R", "\\1", cmd))

source("calib.R")
daisConfigAssim(prior=prior)
daisRunFit()
daisRunAssim(nbatch=nbatch)
daisRunPredict()
save.image(paste(prior, "_", nbatch, ".RData", sep=""))
